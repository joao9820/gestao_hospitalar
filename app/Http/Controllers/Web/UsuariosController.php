    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $user = User::find($id);

        if(!$user)
            return redirect()->route('usuarios')->with('alert', 'Usuário não encontrado');

        if(!$user->destroy($id))
            return redirect()->route('usuarios')->with('alert', 'Não foi possível apagar o usuário');

        return redirect()->route('usuarios')->with('alert', 'Usuário deletado com sucesso');
    }
